atropa.regex.appendPrefixesAndSuffixes = function (word, threshold) {
	var prefixes,
	suffixes;
	prefixes = '(un|re)?';
	suffixes = '(tionally|istic|tion|ance|ence|less|ally|able|ness|ized|ing|ity|ful|ant|ate|est|ist|ic|al|ed|er|et|ly|rs|y|s|r|d)?';
	
	threshold = typeof threshold === 'undefined' ? 2 : threshold;
	
	if (word.length > threshold) {
		word = '\\b' + prefixes + word + suffixes + '\\b';
	} else {
		word = '\\b()' + word + '()\\b';
	}
	return word;
};

atropa.wtf = function (target) {
	var regexValue,
	replacementText,
	x,
	oldWord;
	
	target = target.trim();
	
	for (x in atropa.wtfDict) {
		if (atropa.wtfDict.hasOwnProperty(x)) {
			oldWord = atropa.regex.appendPrefixesAndSuffixes(x);
			regexValue = new RegExp(oldWord, 'gi');
			replacementText = '$1' + atropa.wtfDict[x] + '$2';
			target = target.replace(regexValue, replacementText);
		}
	}
	return target;
};

